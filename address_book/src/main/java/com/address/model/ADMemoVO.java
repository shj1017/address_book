package com.address.model;

public class ADMemoVO {
	private int moSeq;	
	private int mgSeq;	
	private String moTitle;	
	private String moContent;

	public int getMoSeq() {
		return moSeq;
	}
	public void setMoSeq(int moSeq) {
		this.moSeq = moSeq;
	}
	public int getMgSeq() {
		return mgSeq;
	}
	public void setMgSeq(int mgSeq) {
		this.mgSeq = mgSeq;
	}
	public String getMoTitle() {
		return moTitle;
	}
	public void setMoTitle(String moTitle) {
		this.moTitle = moTitle;
	}
	public String getMoContent() {
		return moContent;
	}
	public void setMoContent(String moContent) {
		this.moContent = moContent;
	}
	
}
