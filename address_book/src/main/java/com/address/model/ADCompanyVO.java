package com.address.model;

public class ADCompanyVO {
	private int cpSeq;	
	private String cpName;	
	private String cpMName;	
	private String cpMPhone;	
	private String cpMMail;
	private String cpFax;
	private String cpAddress;	
	private String cpMemo;

	public int getCpSeq() {
		return cpSeq;
	}
	public void setCpSeq(int cpSeq) {
		this.cpSeq = cpSeq;
	}
	public String getCpName() {
		return cpName;
	}
	public void setCpName(String cpName) {
		this.cpName = cpName;
	}
	public String getCpMName() {
		return cpMName;
	}
	public void setCpMName(String cpMName) {
		this.cpMName = cpMName;
	}
	public String getCpMPhone() {
		return cpMPhone;
	}
	public void setCpMPhone(String cpMPhone) {
		this.cpMPhone = cpMPhone;
	}
	public String getCpMMail() {
		return cpMMail;
	}
	public void setCpMMail(String cpMMail) {
		this.cpMMail = cpMMail;
	}
	public String getCpFax() {
		return cpFax;
	}
	public void setCpFax(String cpFax) {
		this.cpFax = cpFax;
	}
	public String getCpAddress() {
		return cpAddress;
	}
	public void setCpAddress(String cpAddress) {
		this.cpAddress = cpAddress;
	}
	public String getCpMemo() {
		return cpMemo;
	}
	public void setCpMemo(String cpMemo) {
		this.cpMemo = cpMemo;
	}
}
